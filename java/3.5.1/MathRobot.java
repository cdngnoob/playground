public class MathRobot {
    public int addition(int x, int y) {
        return x + y;
    }
    
    public int addition(int x, int y, int z) {
        return x + y + z;
    }
    
    public double addition(double x, double y) {
        return x + y;
    }
    
    public double addition(double x, double y, double z) {
        return x + y +z;
    }
}
